    private String caesarDecrypt(String text, int shift) {
        return caesarEncrypt(text, 26 - shift);
    }

    private String caesarEncrypt(String text, int shift) {
        StringBuilder encrypted = new StringBuilder();
        for (char c : text.toCharArray()) {
            if (Character.isLetter(c)) {
                char base = Character.isLowerCase(c) ? 'a' : 'A';
                encrypted.append((char) ((c - base + shift) % 26 + base));
            } else {
                encrypted.append(c);
            }
        }
        return encrypted.toString();
    }

    private boolean authenticateUser(String username, String rawPassword) {
        String filename = "passengers.txt";
        File file = new File(filename);
        if (!file.exists()) return false;

        String encryptedInputPassword = caesarEncrypt(rawPassword, 3);

        try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length >= 2) {
                    String fileUsername = parts[0];
                    String filePassword = parts[1];
                    if (fileUsername.equalsIgnoreCase(username) && filePassword.equals(encryptedInputPassword)) {
                        return true;
                    }
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading user file.");
        }

        return false;
    }
    
    
