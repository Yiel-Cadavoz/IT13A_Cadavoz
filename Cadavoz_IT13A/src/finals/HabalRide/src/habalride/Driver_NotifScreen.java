    private void loadPassengerDetails() {
    try (BufferedReader reader = new BufferedReader(new FileReader("passengers.txt"))) {
        String line;
        String lastLine = null;
        while ((line = reader.readLine()) != null) {
            lastLine = line;
        }

        if (lastLine != null) {
            String[] parts = lastLine.split(",");
            if (parts.length >= 3) {
                Username.setText("Username : " + parts[0]);
                Contact_No.setText("Contact No. : " + parts[2]);
            }
        }

    } catch (IOException e) {
        e.printStackTrace();
    }
}
    
